package Day3;

public class AssignmentOperators {

	public static void main(String[] args) 
	{
		// Assignment = += -= *= /= %=	
		int a = 10;
		a+=5;		// a=a+5;		//15
		System.out.println(a);
		
		a-=5;		//a=a-5;		//10
		System.out.println(a);
		
		a*=2;		//a=a*2;		//20
		System.out.println(a);
		
		a/=2;		//a=a/2;		//10
		System.out.println(a);
		
		a%=2;		//a=a%2;		//0
		System.out.println(a);
		
	}

}
