package day_2;

public class DataTypesDemo {
	
	public static void main(String[] args ) 
	{
		
		int a=100, b=200;
		
		System.out.println(	"the value of a :" + a);
		System.out.println(	"the value of b :" + b);
		System.out.println(	"the sum of a+b :" + (a+b));
		
		byte by = 125;
		System.out.println(by);
		
		short sh = 3535;
		System.out.println(sh);
		
		long l = 123456789L;		// literal is need whether capital or small L
		System.out.println(l);
		
		
		// decimal numbers 
		
		float item_price = 12.4F;		// literal is need whether capital or small F
		System.out.println(item_price);
		
		Double dbl = 1234.121412412;
		System.out.println(dbl);
		
		
		char grade = 'A';
		System.out.println(grade);
		
	
		String name = "Ankit";
		System.out.println(name);
		
		
		boolean bl = false;		// allows only true/false
		System.out.println(bl);
		
		
		
		
	}
}
